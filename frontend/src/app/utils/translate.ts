export function getUrlLabel(url){
  switch (url) {
    case t.url.explore:
      return t.all;
    case t.url.farmers:
      return t.upa;
    case t.url.markets:
    case t.url.fairs:
      return t.markets;
    case t.url.touristic_point:
      return t.touristic_points_title;
    case t.url.initiatives:
      return t.initiatives;
  }
}

export function translateCat(name){
  switch(name){
    case t.upa:case t.url.farmers:
    case t.farmers:case t.farmer:
      return t.categoryName.upa;
    case t.market: case t.markets:
    case t.fairs: case t.url.markets:
      return t.categoryName.market;
    case t.tourism:case t.touristic_point: case t.tourism_rural_experience:
    case t.touristic_points_title:case t.url.touristic_point:
      return t.categoryName.tourism;
    case t.initiative: case t.initiatives_policy:
    case t.url.initiatives:case t.initiatives:
      return t.categoryName.initiative;
    case t.categoryName.upa:
      return t.upa;
    case t.categoryName.market:
      return t.markets;
    case t.categoryName.tourism:
      return t.tourism;
    case t.categoryName.initiative:
      return t.initiatives;
  }
}

export function getTitle(name: string){
  switch(name){
    case t.fairs:
      return t.markets;
    case t.touristic_point:
      return t.tourism_rural_experience;
    case t.farmers:
      return t.upa;
    case t.initiatives:
      return t.initiatives_policy;
    case t.covid:
      return t.corona_special;
  }
}
